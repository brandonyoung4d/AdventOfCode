enum Paper: Character {
    case Empty = "."
    case Filled = "@"
    case Remove = "x"
}

class Grid {
    var grid: Array<Array<Paper>>
    let width: Int
    let height: Int

    init(width: Int, height: Int, defaultValue: Paper) {
        self.width = width
        self.height = height
        self.grid = Array(repeating: Array(repeating: defaultValue, count: height), count: width)
    }

    func set(_ x: Int, _ y: Int, _ value: Paper) {
        grid[x][y] = value
    }

    func isValid(_ x: Int, _ y: Int) -> Bool {
        guard x >= 0, x < width, y >= 0, y < height else {
            return false
        }
        return true
    }

    func get(_ x: Int, _ y: Int) -> Paper {
        grid[x][y]
    }

    func print() {
        for y in (0..<height).reversed() {
            for x in 0..<width {
                Swift.print(grid[x][y].rawValue, terminator: "")
            }
            Swift.print("")
        }
    }

    func clean() {
        for y in 0..<height {
            for x in 0..<width {
                if grid[x][y] == .Remove {
                    grid[x][y] = .Empty
                }
            }
        }
    }
}

fileprivate func convert(_ string: String) -> Grid {
    let rows = string.split(separator: "\n")
    let width = rows.first!.count
    let height = rows.count
    var output = Grid(width: width, height: height, defaultValue: Paper.Empty)

    for (y, row) in rows.enumerated() {
        let adjustedY = height - y - 1
        for (x, char) in row.enumerated() {
            switch char {
            case ".":
                output.set(x, adjustedY, .Empty)
            case "@":
                output.set(x, adjustedY, .Filled)
            default:
                fatalError("Unhandled character: \(char)")
            }
        }
    }

    return output
}

