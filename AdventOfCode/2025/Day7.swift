import Foundation

func year2026Day7Part1() {
    let (manifoldRows, beamStart) = convert()
    var beams: Set<Int> = [beamStart]
    var answer = 0

    for manifoldRow in manifoldRows {
        var newBeams = Set<Int>()
        for beam in beams {
            if manifoldRow.contains(beam) {
                newBeams.insert(beam - 1)
                newBeams.insert(beam + 1)
                answer += 1
            } else {
                newBeams.insert(beam)
            }
        }
        beams = newBeams
    }

    print("Y26 D7 P1: \(answer)")
}

func year2026Day7Part2() {
    let (manifoldRows, beamStart) = convert()
    var beams: Dictionary<Int, Int> = [beamStart: 1]
    var answer = 0

    for manifoldRow in manifoldRows {
        var newBeams = Dictionary<Int, Int>()
        for beam in beams {
            if manifoldRow.contains(beam.key) {
                newBeams[beam.key - 1] = newBeams[beam.key - 1, default: 0] + beam.value
                newBeams[beam.key + 1] = newBeams[beam.key + 1, default: 0] + beam.value
            } else {
                newBeams[beam.key] = newBeams[beam.key, default: 0] + beam.value
            }
        }
        beams = newBeams
    }

    answer = beams.reduce(0, { $0 + $1.value })
    print("Y26 D7 P2 A: \(answer)")
}

fileprivate func convert() -> (Array<Set<Int>>, Int) {
    let lines = puzzleInput.split(separator: "\n")
    let startIndex = lines.first!.firstIndex(of: "S")
    let startNumber = lines.first!.distance(from: lines.first!.startIndex, to: startIndex!)
    var manifoldRows = Array<Set<Int>>()

    for line in lines.dropFirst(2).dropLast() {
        guard line.contains("^") else { continue }

        var manifoldRow = Set<Int>()
        for character in line.enumerated() {
            guard character.element == "^" else { continue }

            manifoldRow.insert(character.offset)
        }
        manifoldRows.append(manifoldRow)
    }

    return (manifoldRows, startNumber)
}

